int main()
{
    DDRB |= (1 << PB5);                 // Configures pin as an output

    while(1) {
        PORTB |= (1 << PB5);            // Turns LED on
        _delay_ms(250);                 // Waits delay
        PORTB &= ~(1 << PB5);           // Turns LED off
        _delay_ms(250);                 // Waits delay
    }

    return 0;
}

